package wrapperClass;

import AllHomeWorks.Day29_Constructor.BookClass;

public class IntroToWrapperClasses {
    public static void main(String[] args) {
        int a = 10;
        Integer aa = 10;

        byte b = 1; // primitive data type
        Byte bb = 1; // object

        short c = 8;
        Short cc = 31000;

        long d = 232;
        Long dd = 25343L;

        float f = 12.4f;
        Float ff = 34234.2f;

        double e = 12.1;
        Double ee = 23.1;

        char g = 'c';
        Character gg = 'd';

        boolean h = true;
        Boolean hh = true;


        //  ---------------------
        // sometimes u need to convert primitive data type into object

        //  boolean --> Boolean = autoboxing  == from primitive to Wrapper class
        //  Boolean --> boolean = unboxing     == vice versa



    }
}
